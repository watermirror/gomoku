/**
 * 方位。
 *
 * @author: zheng.chez
 * @since: 2022/09/22
 */
enum class Direction {

    /**
     * 上。
     */
    NORTH,

    /**
     * 右上。
     */
    NORTHEAST,

    /**
     * 右。
     */
    EAST,

    /**
     * 右下。
     */
    SOUTHEAST,

    /**
     * 下。
     */
    SOUTH,

    /**
     * 左下。
     */
    SOUTHWEST,

    /**
     * 左。
     */
    WEST,

    /**
     * 左上。
     */
    NORTHWEST
}